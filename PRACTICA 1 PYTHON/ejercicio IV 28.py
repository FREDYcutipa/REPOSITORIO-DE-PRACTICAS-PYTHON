# si salimos de puno a las 8 horas y llegamos a ica a las 18 horas
# ¿cual ha sido la velocidaddurante 600km de recorrido? considera la formula V=E/T.

# Definir la distancia total recorrida y el tiempo total de viaje
distancia_total = 600  # en kilómetros
tiempo_total = 10  # en horas (18 - 8)

# Calcular la velocidad promedio
velocidad_promedio = distancia_total / tiempo_total

# Mostrar el resultado
print("La velocidad promedio durante el recorrido de 600 km fue de", velocidad_promedio, "km/h.")